function removeWhiteBackgroundFromLogo() {
  const logoImg = document.querySelector(".logo");
  if (!logoImg) return;

  const cleanLogo = () => {
    const canvas = document.createElement("canvas");
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    canvas.width = logoImg.naturalWidth;
    canvas.height = logoImg.naturalHeight;
    ctx.drawImage(logoImg, 0, 0);

    const imageData = ctx.getImageData(0, 0, canvas.width, canvas.height);
    const data = imageData.data;

    // Detect the background tone from corners (works for white and light-gray backgrounds).
    const corners = [
      [0, 0],
      [canvas.width - 1, 0],
      [0, canvas.height - 1],
      [canvas.width - 1, canvas.height - 1]
    ];
    let br = 0;
    let bg = 0;
    let bb = 0;
    corners.forEach(([x, y]) => {
      const idx = (y * canvas.width + x) * 4;
      br += data[idx];
      bg += data[idx + 1];
      bb += data[idx + 2];
    });
    br /= corners.length;
    bg /= corners.length;
    bb /= corners.length;

    for (let i = 0; i < data.length; i += 4) {
      const r = data[i];
      const g = data[i + 1];
      const b = data[i + 2];

      const dist = Math.sqrt(
        (r - br) * (r - br) +
        (g - bg) * (g - bg) +
        (b - bb) * (b - bb)
      );

      // Hard remove near-background pixels and soften transition to avoid halo edges.
      if (dist < 26) {
        data[i + 3] = 0;
      } else if (dist < 42) {
        const alpha = Math.max(0, Math.min(255, Math.round(((dist - 26) / (42 - 26)) * 255)));
        data[i + 3] = Math.min(data[i + 3], alpha);
      }
    }

    // Remove only dark text in upper area without cutting the hat.
    const titleBand = Math.floor(canvas.height * 0.19);
    for (let y = 0; y < titleBand; y += 1) {
      for (let x = 0; x < canvas.width; x += 1) {
        const idx = (y * canvas.width + x) * 4;
        const rr = data[idx];
        const gg = data[idx + 1];
        const bb = data[idx + 2];
        const isDarkText = rr < 215 && gg < 215 && bb < 215;
        if (isDarkText) data[idx + 3] = 0;
      }
    }

    ctx.putImageData(imageData, 0, 0);

    // Crop transparent margins so the logo sits naturally in header.
    let minX = canvas.width, minY = canvas.height, maxX = 0, maxY = 0;
    for (let y = 0; y < canvas.height; y += 1) {
      for (let x = 0; x < canvas.width; x += 1) {
        const idx = (y * canvas.width + x) * 4;
        if (data[idx + 3] > 0) {
          if (x < minX) minX = x;
          if (y < minY) minY = y;
          if (x > maxX) maxX = x;
          if (y > maxY) maxY = y;
        }
      }
    }
    const hasVisiblePixels = maxX >= minX && maxY >= minY;
    const finalCanvas = hasVisiblePixels ? document.createElement("canvas") : canvas;
    if (hasVisiblePixels) {
      finalCanvas.width = maxX - minX + 1;
      finalCanvas.height = maxY - minY + 1;
      const finalCtx = finalCanvas.getContext("2d");
      if (finalCtx) {
        finalCtx.putImageData(
          ctx.getImageData(minX, minY, finalCanvas.width, finalCanvas.height),
          0,
          0
        );
      }
    }

    finalCanvas.className = "logo-canvas";
    finalCanvas.setAttribute("aria-label", logoImg.alt || "Logo GuiArte");
    logoImg.replaceWith(finalCanvas);
  };

  if (logoImg.complete) cleanLogo();
  else logoImg.addEventListener("load", cleanLogo, { once: true });
}

