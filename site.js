function initMobileNav() {
  const toggle = document.querySelector(".site-nav__toggle");
  const panel = document.getElementById("site-nav-panel");
  if (!toggle || !panel) return;
  const links = panel.querySelectorAll("a");
  function applyToggleLabel(open) {
    const key = open ? "nav_menu_close" : "nav_menu_open";
    toggle.setAttribute("aria-label", t(key));
  }
  function setOpen(open) {
    toggle.setAttribute("aria-expanded", open ? "true" : "false");
    panel.classList.toggle("site-nav__panel--open", open);
    document.body.classList.toggle("site-nav--open", open);
    applyToggleLabel(open);
  }
  applyToggleLabel(false);
  toggle.addEventListener("click", (e) => {
    e.stopPropagation();
    const open = toggle.getAttribute("aria-expanded") === "true";
    setOpen(!open);
  });
  panel.addEventListener("click", (e) => e.stopPropagation());
  links.forEach((a) => a.addEventListener("click", () => setOpen(false)));
  document.addEventListener("click", () => {
    if (toggle.getAttribute("aria-expanded") === "true") setOpen(false);
  });
  document.addEventListener("keydown", (e) => {
    if (e.key !== "Escape") return;
    if (toggle.getAttribute("aria-expanded") === "true") setOpen(false);
  });
}

