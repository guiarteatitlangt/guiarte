function setupActivityDetailsInForm() {
  const selectedContainer = document.getElementById("selectedActivitiesContainer");
  const activityCheckboxes = document.querySelectorAll('input[name="actividad"]');
  const activityScheduleCache = Object.create(null);
  function timeKeyFromLabel(label) {
    if (!label) return "";
    for (let i = 0; i < hourOptionKeys().length; i += 1) {
      const k = hourOptionKeys()[i];
      if (t(k) === label) return k;
      if (I18N.es[k] === label || I18N.en[k] === label || I18N.fr[k] === label) return k;
    }
    return "";
  }

  function hourOptionsHtml(timeKey) {
    const keys = hourOptionKeys();
    const opts = keys
      .map((k) => `<option value="${k}"${timeKey === k ? " selected" : ""}>${t(k)}</option>`)
      .join("");
    return `<option value="">${t("hour_placeholder")}</option>` + opts;
  }

  function escapeAttr(s) {
    return String(s).replace(/&/g, "&amp;").replace(/"/g, "&quot;");
  }

  function snapshotFromDom() {
    selectedContainer.querySelectorAll(".selected-item").forEach((row) => {
      const name = row.getAttribute("data-activity");
      if (!name) return;
      const dateIn = row.querySelector(".activity-date");
      const timeSel = row.querySelector(".activity-time");
      const tk = timeSel && timeSel.value ? timeSel.value : "";
      activityScheduleCache[name] = {
        date: (dateIn && dateIn.value) || "",
        timeKey: tk && tk.indexOf("hour_") === 0 ? tk : timeKeyFromLabel(tk)
      };
    });
  }

  function persistRow(row) {
    const name = row.getAttribute("data-activity");
    if (!name) return;
    const dateIn = row.querySelector(".activity-date");
    const timeSel = row.querySelector(".activity-time");
    const tk = timeSel && timeSel.value ? timeSel.value : "";
    activityScheduleCache[name] = {
      date: (dateIn && dateIn.value) || "",
      timeKey: tk && tk.indexOf("hour_") === 0 ? tk : timeKeyFromLabel(tk)
    };
  }

  function render() {
    snapshotFromDom();
    const selected = Array.from(activityCheckboxes).filter((cb) => cb.checked).map((cb) => cb.value);
    if (!selected.length) {
      selectedContainer.textContent = t("activities_empty");
      return;
    }
    selectedContainer.innerHTML = selected.map((name) => {
      const safe = name.replace(/[^a-zA-Z0-9]/g, "_");
      const raw = activityScheduleCache[name] || { date: "", timeKey: "", time: "" };
      const timeKey = raw.timeKey || timeKeyFromLabel(raw.time || "");
      const dispName = ACT_VALUE_KEYS[name] ? t(ACT_VALUE_KEYS[name]) : name;
      return `
        <div class="selected-item" data-activity="${escapeAttr(name)}">
          <span>${dispName}</span>
          <input id="act_date_${safe}" type="date" class="activity-date" aria-label="${escapeAttr(t("aria_pick_date"))} — ${escapeAttr(dispName)}" value="${raw.date}">
          <select id="act_time_${safe}" class="activity-time" aria-label="${escapeAttr(t("aria_pick_time"))} — ${escapeAttr(dispName)}">
            ${hourOptionsHtml(timeKey)}
          </select>
        </div>
      `;
    }).join("");
  }

  selectedContainer.addEventListener("change", (e) => {
    const row = e.target.closest(".selected-item");
    if (row) persistRow(row);
  });
  selectedContainer.addEventListener("input", (e) => {
    const row = e.target.closest(".selected-item");
    if (row && e.target && e.target.classList && e.target.classList.contains("activity-date")) {
      persistRow(row);
    }
  });

  activityCheckboxes.forEach((cb) => cb.addEventListener("change", render));
  render();
  window.__refreshActivityDetails = render;
}

function getLogoDataUrlForPdf(logoCanvas, logoImg) {
  let source = null;
  try {
    if (logoCanvas && logoCanvas.width && logoCanvas.height) source = logoCanvas;
    else if (logoImg && logoImg.naturalWidth && logoImg.complete !== false) {
      const c = document.createElement("canvas");
      c.width = logoImg.naturalWidth;
      c.height = logoImg.naturalHeight;
      const ctx = c.getContext("2d");
      if (ctx) {
        ctx.drawImage(logoImg, 0, 0);
        source = c;
      }
    }
    if (!source) return "";
    return source.toDataURL("image/png");
  } catch (e) {
    return "";
  }
}

