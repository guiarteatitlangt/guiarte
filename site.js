function splitPath(path) {
  const normalized = (path || "").replace(/\\/g, "/");
  const idx = normalized.lastIndexOf("/");
  if (idx === -1) return { dir: "", name: normalized };
  return { dir: normalized.slice(0, idx), name: normalized.slice(idx + 1) };
}

function splitFileName(fileName) {
  const idx = fileName.lastIndexOf(".");
  if (idx === -1) return { stem: fileName, ext: "" };
  return { stem: fileName.slice(0, idx), ext: fileName.slice(idx) };
}

function buildCandidatePaths(mainPath, extraList) {
  const { dir, name } = splitPath(mainPath);
  const { stem, ext } = splitFileName(name);
  const folder = dir ? dir + "/" : "";
  const cleanExtra = extraList.filter(Boolean);
  const candidates = new Set([mainPath, ...cleanExtra]);

  // Auto-detect numbered images by exact base name for future uploads.
  for (let i = 1; i <= 12; i += 1) {
    candidates.add(folder + stem + i + ext);
    candidates.add(folder + stem + "-" + i + ext);
    candidates.add(folder + stem + " " + i + ext);
    candidates.add(folder + stem + "_" + i + ext);
  }

  return Array.from(candidates);
}

function imageExists(src) {
  return new Promise((resolve) => {
    const img = new Image();
    img.onload = () => resolve(true);
    img.onerror = () => resolve(false);
    img.src = src;
  });
}

async function resolveGallery(photo) {
  const main = photo.dataset.main || photo.getAttribute("src");
  const explicit = (photo.dataset.gallery || "")
    .split(",")
    .map((f) => f.trim())
    .filter(Boolean);
  const candidates = buildCandidatePaths(main, explicit);
  const checks = await Promise.all(candidates.map((src) => imageExists(src)));
  const existing = candidates.filter((_, idx) => checks[idx]);
  return existing.length ? existing : [main];
}

