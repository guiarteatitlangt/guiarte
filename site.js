function syncNavActiveState() {
  const page = document.body && document.body.getAttribute("data-page");
  if (!page) return;
  const activeHrefByPage = { index: "Index.html", tours: "tours.html", book: "reservar.html" };
  const activeHref = activeHrefByPage[page];
  if (!activeHref) return;
  document.querySelectorAll("#site-nav-panel .site-nav__link").forEach((a) => {
    const h = (a.getAttribute("href") || "").trim().replace(/^\.\//, "");
    const isActive =
      h === activeHref ||
      (page === "index" && activeHref.toLowerCase() === h.toLowerCase());
    a.classList.toggle("site-nav__link--active", isActive);
    if (isActive) a.setAttribute("aria-current", "page");
    else a.removeAttribute("aria-current");
  });
}

